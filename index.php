    }else{

      $.post("mostrar.php",
        {
            cedula: $('#cedulaM').val()
        },
        function(data, status){

          let datos = JSON.parse(data);

           if (datos.estatus == "true"){

            $("#digito1").text(datos.resultado.substr(0,1));
            $("#digito2").text(datos.resultado.substr(1,1));
            $("#digito3").text(datos.resultado.substr(2,1));
